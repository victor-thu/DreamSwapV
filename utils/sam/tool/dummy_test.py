import torch

# =========================================
# 配置参数（模拟你的实际场景）
# =========================================
b = 1
c = 1  # 你确认的原始通道数
f_total = 49  # 4n+1 = 5 (n=1)
h, w = 2, 2  # 缩小尺寸方便调试

# =========================================
# 生成假数据（模拟你的 mask_j）
# =========================================
# 创建全零的 mask_j，形状为 [b, c, f_total, h, w]
mask_j = torch.zeros((b, c, f_total, h, w), dtype=torch.bfloat16)

# =========================================
# 你的处理逻辑（完全复现你的代码）
# =========================================
# 处理第一帧
mask_first = mask_j[:, :, :1].repeat(1, 4, 1, 1, 1)  # [1,4,1,2,2]

# 处理剩余帧
mask_remain = (
    mask_j[:, :, 1:]  # [1,1,4,2,2]
    .view(b, c, -1, 4, h, w)  # [1,1,1,4,2,2] (n=1)
    .view(b, c*4, -1, h, w)  # [1,4,1,2,2]
)

# =========================================
# 关键验证点
# =========================================
print("mask_first shape:", mask_first.shape)  # 应为 [1,4,1,2,2]
print("mask_remain shape:", mask_remain.shape)  # 应为 [1,4,1,2,2]

# =========================================
# 尝试拼接
# =========================================
try:
    mask_latents = torch.cat([mask_first, mask_remain], dim=2)
    print("拼接成功！最终形状:", mask_latents.shape)  # 应为 [1,4,2,2,2]
except RuntimeError as e:
    print("拼接失败，错误信息:", e)